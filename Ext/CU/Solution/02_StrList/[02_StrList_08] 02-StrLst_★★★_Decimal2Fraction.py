import math
d = input().strip().split(",")
a = int(d[0])
b1 = int('0'+d[1])
b2 = int(10**(len(d[1])))
bb = math.gcd(b1, b2)
b1 = int(b1//bb)
b2 = int(b2//bb)
c1 = int('0'+d[2])
c2 = int(10**(len(d[2]))-1)*(10**len(d[1]))
cc = math.gcd(b1, b2)
c1 = int(c1//cc)
c2 = int(c2//cc)
x1 = (a * b2 * c2)+(b1*c2)+(c1*b2)
xx = math.gcd(x1, b2*c2)
print(int(x1//xx), "/", int(b2*c2//xx))
